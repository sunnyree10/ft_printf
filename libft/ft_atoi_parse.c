int	ft_atoi_parse(char **s)
{
	int		sign;
	int		result;

	result = 0;
	sign = 1;
	if (**s == '-' || **s == '+')
		sign = 44 - *(*s)++;
	while (**s >= '0' && **s <= '9')
		result = result * 10 + *(*s)++ - '0';
	return (sign * result);
}
